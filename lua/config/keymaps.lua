-- delete current buffer
map("n", "<leader>bd", function()
  local buf = vim.api.nvim_get_current_buf()
  local ft = vim.bo[buf].filetype

  -- If we're in the Snacks explorer or any snacks picker window,
  -- find the last real file buffer and delete that instead
  if ft:match("^snacks_") then
    -- Walk through windows to find a real file buffer
    for _, win in ipairs(vim.api.nvim_list_wins()) do
      local wbuf = vim.api.nvim_win_get_buf(win)
      local wft = vim.bo[wbuf].filetype
      if not wft:match("^snacks_") and vim.bo[wbuf].buftype == "" then
        buf = wbuf
        break
      end
    end
  end

  Snacks.bufdelete(buf)
end, { desc = "Delete Buffer (safe)" })








